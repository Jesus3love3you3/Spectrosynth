def _rotate_glyph_minus90(rows):
    """Tourne un glyphe 8x8 de -90° (anti-horaire)."""
    rotated = [[0]*8 for _ in range(8)]
    for y in range(8):
        for x in range(8):
            bit_on = rows[y] & (1 << (7 - x))
            if bit_on:
                # (x, y) -> (7 - y, x)
                rx = 7 - y
                ry = x
                rotated[ry][rx] = 1
    return rotated

def text90(self, string, x, bottom_y, color=1):
    """
    Affiche le texte verticalement (de haut en bas),
    avec chaque caractère tourné de -90° (anti-horaire).
    """
    for index, ch in enumerate(reversed(string)):
        rows = _font8x8.get(ch.upper(), _font8x8[' '])
        glyph = _rotate_glyph_minus90(rows)
        char_bottom = bottom_y - index * 8

        for gy in range(8):
            for gx in range(8):
                if glyph[gy][gx]:
                    px = x + gx
                    py = char_bottom - (7 - gy)
                    if 0 <= px < self.width and 0 <= py < self.height:
                        self.pixel(px, py, color)

# Injection dans la classe
SSD1306_I2C.text90 = text90
